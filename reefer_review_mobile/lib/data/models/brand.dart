class Brand {
  final int venueId;
  final String name;
  final String location;
  final String type;
  final double rating;
  final int reviewCount;
  final String description;
  final String image;

  Brand({
    required this.venueId,
    required this.name,
    required this.location,
    required this.type,
    required this.rating,
    required this.reviewCount,
    required this.description,
    required this.image,
  });
}
